
public class Player {
	private String name;
	private Ship s;
	private Empire emp;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}

	public Ship getS() {
		return s;
	}

	public void setS(Ship s) {
		this.s = s;
	}

	public Empire getEmp() {
		return emp;
	}

	public void setEmp(Empire emp) {
		this.emp = emp;
	}
	

}
