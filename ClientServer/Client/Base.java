
public class Base {
	private Empire emp;
	private int sX;
	private int sY;
	private int pX;
	private int pY;
	
	public Empire getEmp() {
		return emp;
	}
	
	public void setEmp(Empire emp) {
		this.emp = emp;
	}

	public int getsX() {
		return sX;
	}

	public void setsX(int sX) {
		this.sX = sX;
	}

	public int getsY() {
		return sY;
	}

	public void setsY(int sY) {
		this.sY = sY;
	}

	public int getpX() {
		return pX;
	}

	public void setpX(int pX) {
		this.pX = pX;
	}

	public int getpY() {
		return pY;
	}

	public void setpY(int pY) {
		this.pY = pY;
	}
	
}
